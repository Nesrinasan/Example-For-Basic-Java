public class TrimKullanimi {

    public static void main(String[] args) {

        /**
         * Trim() metodu
         *  --> baştaki ve sonraki boşlukları siler. Aradaki boşlukları silmez.
         */
        String varWithSpaces = " AB CB    ";
        System.out.println (":");
        System.out.println (varWithSpaces);
        System.out.println (":");

        System.out.println (varWithSpaces.trim ());


    }


}
